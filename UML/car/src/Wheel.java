public class Wheel {
    public int size;
    public int pressure;
    public boolean winterReady;
   
    public int getSize() {
        return size;
    }
    public void setSize(int size) {
        this.size = size;
    }
    public int getPressure() {
        return pressure;
    }
    public void setPressure(int pressure) {
        this.pressure = pressure;
    }
    public boolean getWinterReady() {
        return winterReady;
    }
    public void setWinterRady(boolean winterReady) {
        this.winterReady = winterReady;
    }
}
